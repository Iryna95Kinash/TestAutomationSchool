package src.human;

public class Data {

    void nameChange(Human hum, String name) {
        hum.setName(name);
    }

    void ageChange(Human hum, int age) {
        hum.setAge(age);
    }

    void heightChange(Human hum, int height) {
        hum.setHeight(height);
    }

    void cityChange(Human hum, String city) {
        hum.setCity(city);
    }
}
