package src.calculator;

public class Calculator {

    int mult(int number1, int number2) {
        return number1 * number2;
    }

    int div(int number1, int number2) {
        return number1 / number2;
    }

    int sub(int number1, int number2) {
        return number1 - number2;
    }

    int add(int number1, int number2) {
        return number1 + number2;
    }
}
